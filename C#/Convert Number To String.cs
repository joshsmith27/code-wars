// We need a function that can transform a number into a string.
//
// What ways of achieving this do you know?
//
// Examples:
//
// Kata.NumberToString(123) => "123";
// Kata.NumberToString(999) => "999";

using System;

public class Kata
{
  public static string NumberToString(int num)
  {
    return num.ToString();
  }
}
